              <div class="entry-content">
                <p>
                 သင်က ကြောင်ချစ်တတ်တဲ့သူတစ်ဦးလား။ ကိုယ့်အိမ်မှာ ကြောင်လေးတစ်ကောင်လောက် မွေးမယ်လို့ရော စဉ်းစားထားတာလား။ ဒါဆိုရင်တော့ ဒီဆောင်းပါးက သင့်အတွက် အဆင်ပြေစေမှာပါ။ ပထမဆုံးအကြိမ် ကြောင်မွေးတော့မယ့် မိသားစုတွေအတွက် သိထားသင့်တဲ့၊ ပြင်ဆင်ထားသင့်တဲ့အရာတွေကို ဒီဆောင်းပါးမှာ ဖတ်ရှုရင်း ကိုယ့်ကြောင်လေးအတွက် အနွေးထွေးဆုံးအိမ်ဂေဟာလေး ဖြစ်အောင် ဖန်တီးလိုက်ကြရအောင်နော်။

                </p>

                 ကြောင်မွေးတဲ့ဆိုင်တွေနဲ့ ကြောင်အမျိုးအစားတွေကို သေချာစုံစမ်းထားပါ။
ကိုယ့်အိမ်အနီးပတ်ဝန်းကျင်မှာ ဘယ်လိုကြောင်အမျိုးအစားတွေ ရှိလဲ၊ ဘယ်ကြောင်လေးကတော့ ကိုယ်နဲ့ ကိုယ့်မိသားစုအတွက် ကိုက်ညီမလဲပေါ် မူတည်ပြီး သေချာရွေးချယ်ပါ။ ကိုယ်မွေးမယ့် ကြောင်အမျိုးအစားနဲ့ ပတ်သက်ပြီး လိုအပ်တဲ့အရာတွေ၊ သိထားသင့်တဲ့ အချက်တွေကို အွန်လိုင်းကနေဖြစ်စေ၊ ဆိုင်ပိုင်ရှင်ကိုဖြစ်စေ သေချာမေးမြန်းပါ။

                </p>

               
                <p>
                
စွန့်ပစ်ကြောင်ကို မွေးမှာလား။
မြန်မာနိုင်ငံမှာ စွန့်ပစ်ခွေးထက် စွန့်ပစ်ကြောင်အရေအတွက်က ပိုများပါတယ်။ တချို့ကလည်း ပိုင်ရှင်ကိုယ်တိုင်က စွန့်ပစ်လိုက်တာမျိုး၊ တချို့ကြောင်တွေကျ အပြင်လျှောက်သွားရင်းကနေ ကိုယ့်အိမ်ကို မမှတ်မိဘဲ လမ်းပျောက်သွားတာမျိုး ကြုံရတတ်ပါတယ်။ ဒီတော့ စွန့်ပစ်ကြောင်ကို မွေးမယ်ဆိုရင် သူ့ရဲ့ ကျန်းမာရေးအခြေအနေကို စစ်ဆေးထားသင့်ပြီး ပိုပြီးတောင် ဂရုတစိုက်ဆက်ဆံပေးသင့်ပါတယ်။
                </p>
                
                <p>
                 ကိုယ့်အိမ်ကို သေချာရှင်းထားပါ။
ကြောင်တွေက ခွေးတွေလောက် တစ်အိမ်လုံးပတ်ပြေးနေတာမျိုး မဟုတ်ပေမဲ့ အမြင့်ကို ခဏခဏ ခုန်တက်တဲ့အတွက် ပြုတ်ကျပြီးကွဲနိုင်တဲ့အရာတွေကို ဗီဒိုထဲ လုံခြုံစွာ သိမ်းထားဖို့ လိုပါတယ်။ များသောအားဖြင့် ကြောင်တွေက အရှောင်အတိမ်း ကောင်းတဲ့အတွက် ကိုယ့်အိမ်က ရှုပ်ပွနေရင်တောင် သူတို့အတွက် ဘေးအန္တရာယ်မဖြစ်အောင် သေချာနေထိုင်တတ်ပါတယ်။ ပန်းအိုးတွေ ရှိရင်တော့ စင်အမြင့်မှာတင်ထားတာထက် အောက်မှာပဲ ချထားတာ ပိုအဆင်ပြေပါလိမ့်မယ်။

                </p>
                <p>
                 ခွေးအတွက် လိုအပ်တဲ့ ပစ္စည်းတွေအကုန် ဝယ်ယူပြင်ဆင်ထားပါ။
အဓိက လိုအပ်တာတွေကတော့ အိပ်ဖို့အတွက် နေရာ၊ အစာခွက်၊ ရေခွက်တွေပဲ ဖြစ်ပါတယ်။ များသောအားဖြင့် တိရစ္ဆာန်ပစ္စည်းရောင်းတဲ့ဆိုင်တွေမှာ စုံစမ်းဝယ်ယူနိုင်ပါတယ်။ ပြီးရင် ကြောင်ကိုပုံမှန်ကျွေးမယ့် အစာခြောက်တွေကိုပါ တစ်ခါတည်း ဝယ်ထားသင့်ပါတယ်။ ကြောင်တွေက မကြာခဏ ကုတ်ခြစ်ရတာ ကြိုက်တဲ့အတွက် သူတို့အလွယ်တကူ ကုတ်လို့ရမယ့် ပစ္စည်းတစ်ခု ဝယ်ပေးထားပြီး ကိုယ်က သေချာလေ့ကျင့်ပေးထားဖို့ လိုပါတယ်။
                </p>
                <p>တိမွေးကုဆရာဝန် သို့မဟုတ် ဆေးခန်းကို ကြိုရှာထားပါ။
ဘယ်လိုတိရစ္ဆာန်မဆို မွေးတော့မယ်လို့ စဉ်းစားထားတယ်ဆိုရင် တိမွေးကုဆေးခန်း လိပ်စာတွေကို သေချာရှာထားဖို့ အရေးကြီးပါတယ်။ ကိုယ့်အနေနဲ့ ကြောင်မွေးပြီး ရက်သတ္တပတ်အနည်းငယ်အတွင်း တိမွေးကုဆေးခန်းမှာ သွားရောက်စစ်ဆေးကြည့်ရင်း ကြောင်ရဲ့ကျန်းမာရေးနဲ့ လိုအပ်တဲ့ ကာကွယ်ဆေးတွေ ထိုးထားသင့်ပါတယ်။ ဒါ့အပြင် ၃လတစ်ခါလောက် ကိုယ့်ကြောင်ဆီမှာ ကပ်ပါးပိုးကောင်များ ကပ်နေခြင်းရှိမရှိ၊ အရေပြားပြဿနာတွေ၊ အနာတွေ ရှိမရှိကို ပုံမှန်စစ်ဆေးကြည့်သင့်ပါတယ်။
</p>
                <p>ကိုယ့်ရဲ့ တစ်နေ့တာအချိန်တွေထဲက ကြောင်လေးအတွက် အချိန်နည်းနည်းဖယ်ထားပေးပါ။
ကြောင်တွေဟာ လူတွေကို သိပ်ဂရုမစိုက်ဘဲ တစ်ကောင်တည်းသီးသန့် နေတတ်တယ်လို့ ရုတ်တရက် ထင်ရနိုင်ပေမယ့် သက်ရှိသတ္တဝါတွေရဲ့ ထုံးစံက ကိုယ့်အနီးအနားမှာ ရှိတဲ့ တခြားသက်ရှိရဲ့ ဂရုစိုက်မှု၊ အလေးထားမှုကို လိုချင်တာ သဘာဝပါပဲ။ ကြောင်တွေဟာလည်း ထိုနည်းတူပါပဲ။ ခွေးတွေလောက်တော့ မနက်တိုင်း လမ်းထလျှောက်တာမျိုး လုပ်ပေးဖို့ မလိုပေမဲ့ သူတို့ခေါင်းကို ပွတ်သပ်ပေးတာ၊ စကားစမြည်ပြောတာ၊ အချိန်မှန် အစာ၊ ရေတိုက်ကျွေးတာ၊ ရေချိုးသန့်စင်ပေးတာမျိုး လုပ်ပေးဖို့ လိုပါတယ်။</p>
                <p>တိမွေးကုအဖွဲ့တွေမှာ အမြဲပါဝင်လေ့လာနေပါ။
လူမှုကွန်ယက်တွေမှာ တိရစ္ဆာန်ချစ်သူတွေပဲ သီးသန့်ရှိတဲ့ တိမွေးကုအဖွဲ့တွေ၊ တိရစ္ဆာန်ချစ်သူများအဖွဲ့တွေ ရှိပါတယ်။ ဒီအဖွဲ့တွေမှာ အမြဲဝင်ရောက်ကြည့်ရှုပြီး ကိုယ့်ကြောင်အတွက် တစ်ခုခု အသုံးလိုတာ ဝယ်ယူဖို့၊ အကြံဉာဏ်တောင်းဖို့၊ ဒါမှမဟုတ်လည်း ကိုယ့်ရဲ့ ချစ်စရာကောင်းတဲ့ ကြောင်ပုံလေးတွေ တင်တာမျိုး အစရှိသဖြင့် အကုန်လုပ်လို့ ရပါတယ်။
</p>
              <p>ကြောင်မွေးဖို့ မှတ်ပုံတင်လုပ်ပါ။
အရင်နှစ်တွေမှာ မှတ်ပုံတင်ကိစ္စတွေကို ဆောင်ရွက်နေဆဲဖြစ်ပေမယ့် ယခုနှစ် ၂၀၂၀ မှာစပြီး အိမ်မွေးကြောင်အတွက် မှတ်ပုံတင်လုပ်ဖို့ လိုအပ်ပါတယ်။ အဓိက ကိုယ့်ရဲ့ ကြောင်ကို ဂရုတစိုက်မွေးမြူမယ့်အကြောင်း ရပ်ကွက်ထောက်ခံစာ၊ ကြောင်ရဲ့ အရှေ့ပိုင်းနဲ့ ဘေးဘက် ဓာတ်ပုံတစ်ပုံစံကို စည်ပင်ဌာနမှာ ပြပြီး တရားဝင်မှတ်ပုံတင်ဖို့ လိုအပ်ပါတယ်။
</p>        
            <p>ကြောင်တွေထဲမှာ အချိန်တိုနဲ့ လူတွေနဲ့ ရင်းနှီးသွားတာမျိုးလည်း ရှိသလို အချိန်အတော်ကြာပေးပြီးမှ အိမ်အသစ်နဲ့ အသားကျသွားတာမျိုးလည်း ရှိပါတယ်။ တတ်နိုင်သလောက် အချိန်ပေးပြီး သူတို့တွေကို သေချာဂရုစိုက်ပေးပါ။ အချိန်ပေးပါ။ လိုအပ်တဲ့အရာတွေကို ကိုယ့်ဘက်ကနေ အတတ်နိုင်ဆုံး ဖြည့်ဆည်းပေးပါ။
</p>
            <p>ပရိတ်သတ်ကြီးလည်း ကျန်းမာပျော်ရွှင်ပြီး စိတ်ချမ်းသာစရာကောင်းတဲ့ ဘဝနေ့ရက်များကို ပိုင်ဆိုင်ရပါစေလို့ ဆုတောင်းပေးရင်း ဒီမှာပဲ နိဂုံးချုပ်လိုက်ပါတယ်။</p>

